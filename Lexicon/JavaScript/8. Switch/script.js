let color = 'vit';


switch(color) {
    
    case 'red':
        console.log('The color is red');
        break

    case 'blue':
        console.log('The color is blue');
        break

    case 'green':
        console.log('The color is green');
        break

    case 'yellow':
        console.log('The color is yellow');
        break

    case 'purple':
        console.log('The color is purple');
        break

    default: console.log('Färgen är ingen av alternativen');
}